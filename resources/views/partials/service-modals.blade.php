<script>
document.addEventListener('DOMContentLoaded', function() {
    // Service details functionality
    document.querySelectorAll('.service-details-btn').forEach(btn => {
        btn.addEventListener('click', function(e) {
            e.preventDefault();
            const service = this.getAttribute('data-service');
            let modalId = '';
            
            switch(service) {
                case 'web-development':
                    modalId = 'webDevelopmentModal';
                    break;
                case 'mobile-apps':
                    modalId = 'mobileAppsModal';
                    break;
                case 'integrated-systems':
                    modalId = 'integratedSystemsModal';
                    break;
                default:
                    modalId = 'webDevelopmentModal'; // fallback
            }
            
            if (modalId) {
                const modal = new bootstrap.Modal(document.getElementById(modalId));
                modal.show();
            }
        });
    });

    // Request consultation functionality
    document.querySelectorAll('.request-consultation-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            // Close current modal
            const openModal = document.querySelector('.modal.show');
            if (openModal) {
                const modal = bootstrap.Modal.getInstance(openModal);
                modal.hide();
            }
            
            // Scroll to contact section
            setTimeout(() => {
                const contactSection = document.getElementById('contact');
                if (contactSection) {
                    contactSection.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            }, 300);
        });
    });

    // Consultation button functionality
    document.querySelectorAll('.consultation-btn').forEach(btn => {
        btn.addEventListener('click', function(e) {
            e.preventDefault();
            
            // Scroll to contact section
            const contactSection = document.getElementById('contact');
            if (contactSection) {
                contactSection.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            } else {
                // If no contact section on current page, go to contact page
                window.location.href = '/contact';
            }
        });
    });

    // Consultation form submission handler
    const consultationForm = document.getElementById('consultationForm');
    if (consultationForm) {
        consultationForm.addEventListener('submit', function(e) {
            // Allow form to submit normally, but show success message temporarily
            setTimeout(() => {
                // This will be shown after form submission if we stay on the same page
                const formData = new FormData(this);
                console.log('تم إرسال طلب الاستشارة بنجاح');
            }, 100);
        });
    }
});
</script>